    public Prescription(int id) {
        initComponents();
        DBConnection myDB = new DBConnection();
        try {
            PreparedStatement ps = myDB.connection.prepareStatement("select * from patient where ID = ?");
            ps.setString(1, Integer.toString(id));
            rs = ps.executeQuery();
            while(rs.next()){
                jLabel5.setText(rs.getString("patientName"));
                jLabel6.setText(rs.getString("medicine1"));
                jLabel7.setText(rs.getString("dose1"));
                jLabel8.setText(rs.getString("medicine2"));
                jLabel9.setText(rs.getString("dose2"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Prescription.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.setVisible(true);
    }

